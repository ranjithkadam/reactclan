/*
React is a JavaScript library used to build user interfaces (what users see and click on). 
    => It’s mainly used for building single-page applications.
    => React was introduced in 2013.
    => It was created by Facebook and first released to the public at JSConf US 2013.

Key ideas:
    *Component-based: You build the UI using small, reusable pieces called components.
    *Declarative: You tell React what the UI should look like, and React updates it when data changes.
    *Fast: React updates only what changes, not the whole page.

Features:
    1. Component-based thinking was new: Breaking the UI into small, reusable components changed how many developers built apps.
    2. Virtual DOM: React introduced this idea to make updates faster by changing only what’s needed, not the whole page.
    3. JSX was controversial (but powerful): Mixing HTML-like code with JavaScript felt weird at first, but it made UI code clearer and easier to manage.
    4. One-way data flow: This made apps more predictable and easier to debug.

*/